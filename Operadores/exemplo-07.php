<?php
//Incremento e Decremento

$a = 10;

echo $a++; // Somando mais um ao valor da varíavel

echo "<br>";

echo $a; // Mostrando o valor de $a depois de incrementado

echo "<br>";

$a = 10;

echo ++$a; // Incrementa antes de fazer a operação

echo "<br>";

echo --$a; //Decrementa o valor de $a


?>